-- Tabla 1: Productos del negocio
CREATE TABLE inventario_celulares (
    celular_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    modelo VARCHAR2(100) NOT NULL,
    stock INT NOT NULL,
    precio NUMBER(10,2) NOT NULL,
    costo_interno NUMBER(10,2),
    CONSTRAINT chk_stock_no_negativo CHECK (stock >= 0)
);

-- Tabla 2: Servicios prestados
CREATE TABLE detalles_mantenimiento (
    servicio_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    celular_id INT,
    tecnico VARCHAR2(100) NOT NULL,
    repuestos_usados INT DEFAULT 0,
    fecha_servicio TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    costo_repuestos NUMBER(10,2),
    CONSTRAINT fk_celular_servicio
        FOREIGN KEY (celular_id) REFERENCES inventario_celulares(celular_id)
        ON DELETE CASCADE
);

-- Tabla de Control: Simulación de Contexto de Sesión para DCL
CREATE TABLE sesion_simulada (
    usuario_activo VARCHAR2(50),
    rol_activo VARCHAR2(50)
);

