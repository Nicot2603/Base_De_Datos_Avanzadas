-- TRIGGER: Gestión de Inventario mediante Mantenimientos
CREATE OR REPLACE TRIGGER trg_procesar_mantenimiento
AFTER INSERT ON detalles_mantenimiento
FOR EACH ROW
BEGIN
    IF :NEW.repuestos_usados > 0 THEN
        UPDATE inventario_celulares
        SET stock = stock - :NEW.repuestos_usados
        WHERE celular_id = :NEW.celular_id;
    END IF;
END;
/

-- STORED PROCEDURE: Venta Atómica con Manejo Controlado de Excepciones
CREATE OR REPLACE PROCEDURE sp_registrar_venta(
    p_celular_id IN INT,
    p_cantidad IN INT
)
IS
    v_stock_actual INT;
    stock_insuficiente EXCEPTION;
    error_interno EXCEPTION;
    PRAGMA EXCEPTION_INIT(error_interno, -20002);
BEGIN
    -- Bloqueo explícito de fila (Pessimistic Locking)
    SELECT stock INTO v_stock_actual 
    FROM inventario_celulares 
    WHERE celular_id = p_celular_id FOR UPDATE;
    
    IF v_stock_actual >= p_cantidad THEN
        UPDATE inventario_celulares
        SET stock = stock - p_cantidad
        WHERE celular_id = p_celular_id;
        COMMIT;
    ELSE
        RAISE stock_insuficiente;
    END IF;

EXCEPTION
    WHEN stock_insuficiente THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(-20001, 'Stock insuficiente para la venta.');
    WHEN OTHERS THEN
        ROLLBACK;
        RAISE_APPLICATION_ERROR(-20002, 'Transacción abortada: Error interno de base de datos.');
END;
/

-- PROCEDIMIENTO: Manejo Contextual del Cambio de Roles Dinámicos
CREATE OR REPLACE PROCEDURE sp_cambiar_sesion(
    p_usuario IN VARCHAR2,
    p_rol IN VARCHAR2
) IS
BEGIN
    UPDATE sesion_simulada 
    SET usuario_activo = UPPER(p_usuario), 
        rol_activo = UPPER(p_rol);
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('------------------------------------------------------------');
    DBMS_OUTPUT.PUT_LINE('CAMBIO DE CONTEXTO DE SEGURIDAD EXITOSO');
    DBMS_OUTPUT.PUT_LINE('Usuario Activo: ' || UPPER(p_usuario) || ' | Rol Asignado: ' || UPPER(p_rol));
    DBMS_OUTPUT.PUT_LINE('------------------------------------------------------------');
END;
/


