-- Vista 1: Catálogo Público (Clientes)
CREATE OR REPLACE VIEW vista_cliente_catalogo AS
SELECT
    celular_id,
    modelo,
    precio AS precio_venta,
    CASE
        WHEN stock = 0 THEN 'Agotado'
        WHEN stock <= 2 THEN 'Últimas unidades'
        ELSE 'En stock'
    END AS disponibilidad
FROM inventario_celulares
WHERE stock >= 0;

-- Vista 2: Panel Operativo (Técnicos)
CREATE OR REPLACE VIEW vista_tecnico_panel AS
SELECT
    ic.celular_id,
    ic.modelo,
    ic.stock AS repuestos_disponibles,
    dm.servicio_id,
    dm.tecnico,
    dm.repuestos_usados,
    dm.fecha_servicio,
    dm.costo_repuestos
FROM inventario_celulares ic
LEFT JOIN detalles_mantenimiento dm ON ic.celular_id = dm.celular_id
ORDER BY dm.fecha_servicio DESC;

-- Vista 3: Reporte General Crudo (Admin Base)
CREATE OR REPLACE VIEW vista_gerencial_reporte AS
SELECT
    ic.modelo,
    ic.stock AS stock_actual,
    ic.precio AS precio_venta,
    ic.costo_interno,
    ROUND(((ic.precio - ic.costo_interno) / ic.precio) * 100, 1) AS margen_pct,
    COUNT(dm.servicio_id) AS total_servicios,
    NVL(SUM(dm.repuestos_usados), 0) AS repuestos_consumidos,
    NVL(SUM(dm.costo_repuestos), 0) AS gasto_repuestos,
    CASE
        WHEN ic.stock = 0 THEN 'CRÍTICO: Sin stock'
        WHEN ic.stock <= 2 THEN 'ALERTA: Stock bajo'
        ELSE 'Normal'
    END AS estado_inventario
FROM inventario_celulares ic
LEFT JOIN detalles_mantenimiento dm ON ic.celular_id = dm.celular_id
GROUP BY ic.modelo, ic.stock, ic.precio, ic.costo_interno;

-- Vista 4: Reporte con Ofuscación por Rol (Simulación DCL Dinámica)
CREATE OR REPLACE VIEW vista_gerencial_segura AS
SELECT
    ic.modelo,
    ic.stock AS stock_actual,
    CASE 
        WHEN (SELECT rol_activo FROM sesion_simulada FETCH FIRST 1 ROWS ONLY) = 'ROL_ADMIN' THEN ic.precio 
        ELSE NULL 
    END AS precio_venta,
    CASE 
        WHEN (SELECT rol_activo FROM sesion_simulada FETCH FIRST 1 ROWS ONLY) = 'ROL_ADMIN' THEN ic.costo_interno 
        ELSE NULL 
    END AS costo_interno,
    CASE 
        WHEN (SELECT rol_activo FROM sesion_simulada FETCH FIRST 1 ROWS ONLY) = 'ROL_ADMIN' THEN ROUND(((ic.precio - ic.costo_interno) / ic.precio) * 100, 1)
        ELSE 0 
    END AS margen_pct
FROM inventario_celulares ic;
/


